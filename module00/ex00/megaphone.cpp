#include <iostream>

int main(int ac, char **av)
{
    int i;
    int x;

    i = 0;
    x = 1;
    while (x < ac)
    {
        i = 0;
        while (av[x][i])
        {
            std::cout << (char)toupper(av[x][i]);
            i++;
        }
        x++;
    }
}